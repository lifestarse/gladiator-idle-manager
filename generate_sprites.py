def _apply_tier_tint(img, tier):
    """Apply tier-based color tint to an enemy sprite."""
    tints = {
        1: (0.10, 0.05, -0.05),     # earthy brown: warm shift
        2: (-0.10, 0.15, -0.10),     # mossy green
        3: (-0.05, 0.0, 0.15),       # steel blue
        4: (0.10, -0.10, 0.15),      # dark purple
        5: (0.20, -0.15, -0.15),     # blood red
    }
    tr, tg, tb = tints.get(tier, (0, 0, 0))
    w, h = img.size
    result = img.copy()
    for y in range(h):
        for x in range(w):
            px = result.getpixel((x, y))
            if px[3] == 0:
                continue
            r = max(0, min(255, int(px[0] * (1 + tr))))
            g = max(0, min(255, int(px[1] * (1 + tg))))
            b = max(0, min(255, int(px[2] * (1 + tb))))
            result.putpixel((x, y), (r, g, b, px[3]))
    return result


