def _add_rarity_border(img, rarity):
    """Add rarity-based border treatment to an item icon."""
    w, h = img.size
    result = img.copy()
    draw = ImageDraw.Draw(result)

    rarity_colors = {
        'common': None,
        'uncommon': c('green'),
        'rare': c('blue_hi'),
        'epic': c('purp_hi'),
        'legendary': c('gold_hi'),
    }

    color = rarity_colors.get(rarity)
    if color is None:
        return result

    border_width = 2 if rarity in ('epic', 'legendary') else 1

    # Draw border
    for i in range(border_width):
        draw.rectangle([i, i, w - 1 - i, h - 1 - i], outline=color)

    # Corner ornaments for legendary
    if rarity == 'legendary':
        gold = c('gold')
        gold_hi = c('gold_hi')
        # Top-left
        for dx, dy in [(2, 2), (3, 2), (2, 3)]:
            result.putpixel((dx, dy), gold_hi)
        # Top-right
        for dx, dy in [(-3, 2), (-4, 2), (-3, 3)]:
            result.putpixel((w + dx, dy), gold_hi)
        # Bottom-left
        for dx, dy in [(2, -3), (3, -3), (2, -4)]:
            result.putpixel((dx, h + dy), gold_hi)
        # Bottom-right
        for dx, dy in [(-3, -3), (-4, -3), (-3, -4)]:
            result.putpixel((w + dx, h + dy), gold_hi)

    return result


def generate_items(base_dir):
    """Generate all 20 item icon files."""
    print("Generating item icons...")
    out = os.path.join(base_dir, "items")
    count = 0

    generators = {
        'weapon': _draw_weapon_icon,
        'armor': _draw_armor_icon,
        'accessory': _draw_accessory_icon,
        'relic': _draw_relic_icon,
    }

    rarities = ['common', 'uncommon', 'rare', 'epic', 'legendary']

    for slot, gen_func in generators.items():
        base_img = gen_func()
        for rarity in rarities:
            final = _add_rarity_border(base_img, rarity)
            final.save(os.path.join(out, f"{slot}_{rarity}.png"))
            count += 1
        print(f"  {slot}: {', '.join(rarities)}")

    return count


