def c(name):
    """Get a palette color as RGBA tuple."""
    col = P[name]
    if len(col) == 4:
        return col
    return col + (255,)


# Shorthand color references for pixel maps
_ = '_'          # transparent
O = 'outline'    # outline/black
K = 'black'


def render_pixel_map(pixel_map, palette_map, size=None):
    """
    Render a 2D pixel map to a PIL Image.
    pixel_map: list of strings, each char maps via palette_map to a color name.
    palette_map: dict mapping single chars to palette key names.
    """
    h = len(pixel_map)
    w = max(len(row) for row in pixel_map)
    if size is None:
        size = (w, h)
    img = Image.new("RGBA", size, (0, 0, 0, 0))
    for y, row in enumerate(pixel_map):
        for x, ch in enumerate(row):
            if ch == ' ' or ch == '.':
                continue
            color_name = palette_map.get(ch)
            if color_name is None or color_name == '_':
                continue
            img.putpixel((x, y), c(color_name))
    return img


def make_dirs():
    """Create all output directories."""
    base = os.path.join(os.path.dirname(os.path.abspath(__file__)), "sprites")
    dirs = [
        "fighters", "enemies", "items", "nav", "icons", "bg", "zones"
    ]
    for d in dirs:
        os.makedirs(os.path.join(base, d), exist_ok=True)
    return base


