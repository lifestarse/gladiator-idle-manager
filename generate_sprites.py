def _draw_bg_default(w, h):
    """Stone cross-hatch pattern, dark neutral."""
    img = Image.new("RGBA", (w, h), (38, 36, 42, 255))
    draw = ImageDraw.Draw(img)

    # Base gradient
    for y in range(h):
        v = 35 + int(y / h * 10)
        draw.line([(0, y), (w, y)], fill=(v, v - 2, v + 2, 255))

    # Cross-hatch stone pattern
    spacing = 16
    for y in range(0, h, spacing):
        for x in range(0, w, spacing):
            # Horizontal lines
            v = 42 + ((x + y) * 7 % 8)
            draw.line([(x, y), (x + spacing, y)], fill=(v, v - 2, v + 2, 255))
            # Vertical lines
            draw.line([(x, y), (x, y + spacing)], fill=(v - 2, v - 4, v, 255))
            # Small highlight at intersections
            if (x // spacing + y // spacing) % 3 == 0:
                img.putpixel((x, y), (v + 8, v + 6, v + 10, 255))

    # Subtle border darkening (vignette)
    for y in range(h):
        for x in range(w):
            dx = min(x, w - x) / (w / 2)
            dy = min(y, h - y) / (h / 2)
            dist = min(dx, dy)
            if dist < 0.15:
                px = img.getpixel((x, y))
                factor = dist / 0.15
                r = int(px[0] * (0.7 + 0.3 * factor))
                g = int(px[1] * (0.7 + 0.3 * factor))
                b = int(px[2] * (0.7 + 0.3 * factor))
                img.putpixel((x, y), (r, g, b, 255))

    return img


