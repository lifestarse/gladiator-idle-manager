def _make_attack_frame(idle_img):
    """
    Generate attack frame from idle: shift weapon arm forward, lean body slightly.
    Uses pixel manipulation to create an action pose variant.
    """
    w, h = idle_img.size
    atk = Image.new("RGBA", (w, h), (0, 0, 0, 0))

    # Split image into left weapon zone and body
    # Shift left portion (weapon) right by 2px and up by 1px
    # Shift body slightly forward (right 1px)
    for y in range(h):
        for x in range(w):
            px = idle_img.getpixel((x, y))
            if px[3] == 0:
                continue
            # Upper body leans forward slightly
            if y < h // 2:
                nx = min(x + 1, w - 1)
                ny = max(y - 1, 0)
            else:
                nx = x
                ny = y
            # Weapon side shifts more
            if x < w // 3:
                nx = min(x + 3, w - 1)
                ny = max(y - 2, 0)
            if 0 <= nx < w and 0 <= ny < h:
                existing = atk.getpixel((nx, ny))
                if existing[3] == 0:
                    atk.putpixel((nx, ny), px)
    return atk


def _make_hurt_frame(idle_img):
    """
    Generate hurt frame from idle: recoil back, slight red tint flash.
    """
    w, h = idle_img.size
    hurt = Image.new("RGBA", (w, h), (0, 0, 0, 0))

    for y in range(h):
        for x in range(w):
            px = idle_img.getpixel((x, y))
            if px[3] == 0:
                continue
            # Shift everything back (left 2px) and tilt
            nx = max(x - 2, 0)
            if y < h // 2:
                ny = y
                nx = max(x - 1, 0)
            else:
                ny = y
                nx = max(x - 2, 0)
            # Add slight red tint for hurt flash
            r = min(px[0] + 30, 255)
            g = max(px[1] - 15, 0)
            b = max(px[2] - 15, 0)
            if 0 <= nx < w and 0 <= ny < h:
                hurt.putpixel((nx, ny), (r, g, b, px[3]))
    return hurt


