def _draw_bg_arena(w, h):
    """Stone arena with columns, dark."""
    img = Image.new("RGBA", (w, h), (35, 30, 40, 255))
    draw = ImageDraw.Draw(img)

    # Stone floor gradient (bottom third)
    floor_y = h * 2 // 3
    for y in range(floor_y, h):
        brightness = 40 + int((y - floor_y) / (h - floor_y) * 20)
        draw.line([(0, y), (w, y)], fill=(brightness, brightness - 5, brightness + 5, 255))

    # Stone tile pattern on floor
    tile_size = 24
    for ty in range(floor_y, h, tile_size):
        for tx in range(0, w, tile_size):
            offset = (tile_size // 2) if ((ty - floor_y) // tile_size) % 2 else 0
            sx = tx + offset
            # Tile border lines
            draw.line([(sx, ty), (sx + tile_size, ty)], fill=(55, 50, 60, 255))
            draw.line([(sx, ty), (sx, ty + tile_size)], fill=(55, 50, 60, 255))
            # Random highlight pixels inside tile
            cx, cy = sx + tile_size // 2, ty + tile_size // 2
            if (cx * 7 + cy * 13) % 17 < 3:
                draw.rectangle([cx - 1, cy - 1, cx, cy], fill=(65, 60, 70, 255))

    # Columns (left and right)
    col_w = 28
    for col_x in [30, w - 58]:
        # Column base
        draw.rectangle([col_x - 4, floor_y - 10, col_x + col_w + 4, floor_y + 8],
                       fill=(50, 45, 55, 255), outline=(60, 55, 65, 255))
        # Column shaft
        for y in range(80, floor_y - 10):
            shade = 45 + int(math.sin(y * 0.05) * 8)
            draw.line([(col_x, y), (col_x + col_w, y)],
                      fill=(shade, shade - 3, shade + 5, 255))
        # Column highlight stripe
        for y in range(80, floor_y - 10):
            img.putpixel((col_x + 6, y), (65, 60, 75, 255))
            img.putpixel((col_x + 7, y), (70, 65, 80, 255))
        # Column capital
        draw.rectangle([col_x - 6, 72, col_x + col_w + 6, 80],
                       fill=(55, 50, 60, 255), outline=(65, 60, 70, 255))
        # Column base detail
        draw.rectangle([col_x - 6, floor_y - 18, col_x + col_w + 6, floor_y - 10],
                       fill=(55, 50, 60, 255), outline=(65, 60, 70, 255))

    # Dark sky / ceiling area with subtle arch
    for y in range(0, 72):
        brightness = 25 + int(y / 72 * 10)
        draw.line([(0, y), (w, y)], fill=(brightness, brightness - 3, brightness + 3, 255))

    # Arch at top
    cx = w // 2
    for angle_deg in range(0, 181, 2):
        angle = math.radians(angle_deg)
        ax = int(cx + 140 * math.cos(angle))
        ay = int(70 - 50 * math.sin(angle))
        if 0 <= ax < w and 0 <= ay < h:
            draw.rectangle([ax - 1, ay - 1, ax + 1, ay + 1], fill=(55, 50, 60, 255))

    # Scattered sand/dirt pixels on floor
    for i in range(200):
        px = (i * 137 + 43) % w
        py = floor_y + (i * 89 + 17) % (h - floor_y)
        if 0 <= px < w and 0 <= py < h:
            v = 55 + (i * 7) % 20
            img.putpixel((px, py), (v + 10, v + 5, v - 5, 255))

    return img


def _draw_bg_forge(w, h):
    """Workshop, warm orange tones."""
    img = Image.new("RGBA", (w, h), (50, 30, 20, 255))
    draw = ImageDraw.Draw(img)

    # Warm gradient background
    for y in range(h):
        r = 40 + int(y / h * 25)
        g = 25 + int(y / h * 12)
        b = 15 + int(y / h * 8)
        draw.line([(0, y), (w, y)], fill=(r, g, b, 255))

    # Brick wall pattern (top 2/3)
    brick_h = 12
    brick_w = 28
    for by in range(0, h * 2 // 3, brick_h):
        offset = brick_w // 2 if (by // brick_h) % 2 else 0
        for bx in range(-brick_w, w + brick_w, brick_w):
            sx = bx + offset
            # Mortar lines
            draw.line([(sx, by), (sx + brick_w, by)], fill=(35, 22, 15, 255))
            draw.line([(sx, by), (sx, by + brick_h)], fill=(35, 22, 15, 255))
            # Brick variation
            v = 45 + ((sx * 7 + by * 13) % 20)
            draw.rectangle([sx + 1, by + 1, sx + brick_w - 1, by + brick_h - 1],
                           fill=(v + 15, v, v - 10, 255))

    # Anvil silhouette (center)
    anvil_x = w // 2 - 30
    anvil_y = h // 2 + 50
    draw.rectangle([anvil_x, anvil_y, anvil_x + 60, anvil_y + 8],
                   fill=(50, 50, 55, 255), outline=(60, 60, 65, 255))
    draw.rectangle([anvil_x + 15, anvil_y - 20, anvil_x + 45, anvil_y],
                   fill=(55, 55, 60, 255), outline=(65, 65, 70, 255))
    draw.rectangle([anvil_x + 10, anvil_y + 8, anvil_x + 50, anvil_y + 40],
                   fill=(45, 45, 50, 255), outline=(55, 55, 60, 255))

    # Forge glow (warm orange light source, upper area)
    glow_cx, glow_cy = w // 2, 100
    for y in range(max(0, glow_cy - 80), min(h, glow_cy + 80)):
        for x in range(max(0, glow_cx - 60), min(w, glow_cx + 60)):
            dist = math.sqrt((x - glow_cx) ** 2 + (y - glow_cy) ** 2)
            if dist < 80:
                intensity = max(0, 1 - dist / 80) * 0.3
                px = img.getpixel((x, y))
                r = min(255, int(px[0] + 80 * intensity))
                g = min(255, int(px[1] + 40 * intensity))
                b = min(255, int(px[2] + 10 * intensity))
                img.putpixel((x, y), (r, g, b, 255))

    # Scattered spark pixels
    for i in range(40):
        sx = glow_cx + ((i * 37 - 20) % 80) - 40
        sy = glow_cy + ((i * 53 - 10) % 60) - 30
        if 0 <= sx < w and 0 <= sy < h:
            img.putpixel((sx, sy), (255, 200, 80, 255))

    # Wooden floor (bottom third)
    floor_y = h * 2 // 3
    plank_h = 8
    for py in range(floor_y, h, plank_h):
        v = 55 + ((py // plank_h * 7) % 15)
        draw.rectangle([0, py, w, py + plank_h - 1],
                       fill=(v + 10, v - 5, v - 20, 255))
        draw.line([(0, py), (w, py)], fill=(35, 20, 10, 255))
        # Wood grain
        for gx in range(0, w, 40):
            grain_x = gx + ((py * 3) % 20)
            if 0 <= grain_x < w:
                img.putpixel((grain_x, py + plank_h // 2), (v + 5, v - 10, v - 25, 255))

    return img


